// Framebuffer definition
static uint8_t framebuf[X_BYTES * Y_LINES];

// Ustaw piksel (x,y) na czarny (0)
static void set_pixel(int x, int y){
    if (x < 0 || x >= EPD_WIDTH || y < 0 || y >= EPD_HEIGHT) return;
    int idx = y * X_BYTES + (x >> 3);
    int bit = 7 - (x & 7);
    framebuf[idx] &= ~(1 << bit);
}

