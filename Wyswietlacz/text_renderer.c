static const int FONT_COUNT = sizeof(font5x7) / sizeof(font5x7[0]);

// Initialize renderer: clear framebuff to white
void text_renderer_init(void){
    memset(framebuf, 0xFF, sizeof(framebuf));
}

// Set a single pixel in framebuff to black
void set_pixel(int x, int y){
    if( x < 0 || x >= EPD_WIDTH  ||  y < 0 || y >= EPD_HEIGHT) return;
    x = (EPD_WIDTH - 7) - x;
    y = y + 1;
    int idx = y * X_BYTES + (x >> 3);
    int bit = 7 - (x & 7);
    framebuf[idx] &= ~(1 << bit);
}


void draw_char(int x, int y, char c){
    for (int i = 0; i < FONT_COUNT; i++) {
        if (font5x7[i].c == c){
            for (int col = 0; col < 5; col++){
                uint8_t bits = font5x7[i].data[col];
                for (int row = 0; row < 7; row++){
                    if (bits & (1 << row)){
                        set_pixel(x + col, y + row);
                    }
                }
            }
            return;
        }
    }
}

void draw_logo(){
    int logo_width = 96;
    int logo_height = 32;
    int x_offset = EPD_WIDTH-logo_width - 7;
    int y_offset = EPD_HEIGHT-logo_height - 1;
    int bytes_per_row = (logo_width + 7) / 8;

    for (int y = 0; y < logo_height; y++) {
        for (int xb = 0; xb < bytes_per_row; xb++) {
            uint8_t byte = logo[y * bytes_per_row + xb];
            for (int bit = 0; bit < 8; bit++) {
                int x = xb * 8 + bit;
                if (x >= logo_width) break;
                // MSB first: 0x80 -> leftmost pixel
                if (byte & (0x80 >> bit)) {
                    set_pixel(x_offset + x, y_offset + y);
                }
            }
        }
    }
}



void draw_string(int x0, int y0, const char *s){
    int x = x0;
    int y = y0;
    const int spacing = 7;
    const int line_height = 10; 

    if (x0 < 0) x0 = 0;
    if (y0 < 0) y0 = 0;

    while (*s) {
        if (y >= EPD_HEIGHT) return;

        if (*s == '\n') {
            x = x0;
            y += line_height;
            s++;
            continue;
        }

        if (*s == ' ') {
            x += spacing;
            s++;
            if (x + spacing + 7 > EPD_WIDTH) {
                x = x0;
                y += line_height;
            }
            continue;
        }

        const char *p = s;
        while (*p && *p != ' ' && *p != '\n' && *p != '\r') p++;
        int word_len = p - s;

        while (word_len > 0){
            if (y >= EPD_HEIGHT) return;

            int space_px = EPD_WIDTH - x;
            int chars_fit = space_px / spacing;
            if (chars_fit <= 0) {
                x = x0;
                y += line_height;
                continue;
            }

            int to_draw = (word_len <= chars_fit) ? word_len : chars_fit;

            for (int i = 0; i < to_draw; ++i) {
                draw_char(x, y, s[i]);
                x += spacing;
            }
            s += to_draw;
            word_len -= to_draw;

            if (word_len > 0) {
                x = x0;
                y += line_height;
            }
            else{
                if (*s == ' ') {
                    x += spacing;
                    s++;
                    if (x + spacing + 7 > EPD_WIDTH) {
                        x = x0;
                        y += line_height;
                    }
                }
                break;
            }
        }
    }
}

static void printInTerminal(){
    for (int yy = 0; yy < Y_LINES; yy++) {
        for (int xb = 0; xb < X_BYTES; xb++) {
            size_t idx = yy * X_BYTES + xb;
            if (idx >= X_BYTES * Y_LINES) {
                fprintf(stderr, "Out of bounds index %zu\n", idx);
                return;
            }
            uint8_t byte = framebuf[idx];
            for (int bit = 7; bit >= 0; bit--) {
                putchar((byte & (1 << bit)) ? ' ' : 'x');
            }
        }
        putchar('\n');
    }
}